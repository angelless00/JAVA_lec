/****************************************************
 * 배열(Array) 선언 및 활용
 * : 동일한 데이터 타입의 데이터를 연속된 메모리 공간에 저장하는 것
 *****************************************************/
public class Ex_Array05 {

	public static void main(String[] args) {
		// 배열 객체 생성
		int values[]=new int[5];
		
		// 배열 객체명.속성명
		System.out.println("변수의 원소 갯수"+values.length);
		
		// 배열 객체명.메서드명()
		System.out.println(values.toString());
	}
		
	

}
